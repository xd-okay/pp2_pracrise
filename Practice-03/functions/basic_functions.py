# 1. Самая простая функция без параметров
def say_hello():
    """Эта функция просто выводит приветствие."""
    print("Привет! Добро пожаловать в мир функций Python.")

# 2. Функция с одним параметром
def greet_by_name(name):
    """
    Приветствует пользователя по имени.
    :param name: Строка с именем
    """
    print(f"Рад тебя видеть, {name}!")

# 3. Функция с несколькими параметрами
def show_stats(username, level, score):
    """Выводит статистику игрока."""
    print(f"--- Статистика игрока: {username} ---")
    print(f"Уровень: {level}")
    print(f"Очки: {score}")

# --- ВЫЗОВЫ ФУНКЦИЙ ---

# Вызываем простую функцию
say_hello()

# Получаем имя от пользователя и передаем в функцию
user_name = input("Как тебя зовут? ")
greet_by_name(user_name)

# Вызываем функцию с несколькими данными
show_stats("Pro_Coder", 15, 2500)